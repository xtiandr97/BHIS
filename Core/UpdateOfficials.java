    void GetData(String id){
        try {
            DBConnection.init();
            Connection c = (Connection) DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            ps = c.prepareStatement("SELECT * from officials");
            rs = ps.executeQuery();
            while (rs.next()) {
                if (rs.getString(1).equals(id)){
                    txt_ln.setText(rs.getString(2));
                    txt_fn.setText(rs.getString(3));
                    txt_mi.setText(rs.getString(4));
                    cb_gender.setSelectedItem(rs.getString(8));
                    cb_cs.setSelectedItem(rs.getString(7));
                    
                }
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
