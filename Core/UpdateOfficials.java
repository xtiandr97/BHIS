    private void bt_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_updateActionPerformed
        // TODO add your handling code here:
       String p;
       SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
       if (cb_officials.getSelectedIndex() == 0 || cb_officials.getSelectedIndex() == 1 || cb_officials.getSelectedIndex() == 9 || cb_officials.getSelectedIndex() == 10){
           p = (String) cb_officials.getSelectedItem();
       } else {
           p = "Kagawad";
       }
        try {
                DBConnection.init();
                Connection c = (Connection) DBConnection.getConnection();
                PreparedStatement ps, ps1;
                ps = c.prepareStatement("Update officials set id =(?), ln =(?), fn =(?), mi =(?), pstn =(?), prk =(?), cvst =(?), gndr =(?), bday =(?) where id like (?)");
                ps.setString(1, id);
                ps.setString(2, txt_ln.getText());
                ps.setString(3, txt_fn.getText());
                ps.setString(4, txt_mi.getText());
                ps.setString(5, p);
                ps.setString(6, String.valueOf(cb_prk.getSelectedIndex()));
                ps.setString(7, (String) cb_cs.getSelectedItem());
                ps.setString(8, (String) cb_gender.getSelectedItem());
                ps.setString(9, dateFormat.format(this.txt_bday.getDate()));
                ps.setString(10, id);
                ps1 = c.prepareStatement("INSERT INTO logs (user,lvl,actn,dati,info) VALUES" + "('" + this.a.a.a.getUser() + "','" +this.a.a.a.getLevel()+ "','Update Official',DATE_FORMAT(NOW(),'%Y-%m-%d %H:%i:%S'), 'The user updated the official to the system.\nID: " + this.id + "\nName: " + txt_fn.getText() + " " + txt_mi.getText() + " " + txt_ln.getText() + "')");
                int n = JOptionPane.showConfirmDialog(this, "Do you want to update this official", "Confirm Update", JOptionPane.YES_NO_OPTION);
                if (n == 0) {
                    ps.executeUpdate();
                    ps1.execute();
                    JOptionPane.showMessageDialog(this, "You update the official successfully");
                    dispose();
                }
            } catch (Exception e) {
                System.out.println(e);
            }
    }//GEN-LAST:event_bt_updateActionPerformed

