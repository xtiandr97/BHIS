    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Date st = new Date();
        Date en =  new Date();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm:ss");
        String start = (String) dateFormat.format(this.txt_date.getDate()) + " " + (String) timeFormat.format(this.txt_start.getValue());
        String end = (String) dateFormat.format(this.txt_date.getDate()) + " " + (String) timeFormat.format(this.txt_end.getValue());
        DateFormat dF = new SimpleDateFormat("HH:mm:ss");
        try {
            st = dF.parse((String) timeFormat.format(this.txt_start.getValue()));
            en = dF.parse((String) timeFormat.format(this.txt_end.getValue()));
        } catch (ParseException e) {
            System.out.println(e);
        }
        if (txt_title.getText().isEmpty()){
            txt_w.setText("*Title is required");
        } else if (st.after(en) || st.equals(en)){
            txt_w.setText("*Invalid time schedule");
        } else if (checkConflict(this.a.id,start, end)){
            JOptionPane.showMessageDialog(this, "Sorry! Their is a conflict of schedule in the events.", "Event Conflict", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                DBConnection.init();
                Connection c = (Connection) DBConnection.getConnection();
                PreparedStatement ps, ps1;
                ps = c.prepareStatement("Update events set ID =(?), ev_name =(?), ev_desc =(?), ev_start =(?), ev_end =(?), ev_perm =(?) where ID like (?)");
                ps.setString(1, this.a.id);
                ps.setString(2, txt_title.getText());
                ps.setString(3, txt_desc.getText());
                ps.setString(4, start);
                ps.setString(5, end);
                ps.setString(6, this.a.a.a.getName());
                ps.setString(7, this.a.id);
                ps1 = c.prepareStatement("INSERT INTO logs (user,lvl,actn,dati,info) VALUES" + "('" + this.a.a.a.getUser() + "','" +this.a.a.a.getLevel()+ "','Update Event',DATE_FORMAT(NOW(),'%Y-%m-%d %H:%i:%S'), 'The user updated event to the system.')");
                int n = JOptionPane.showConfirmDialog(this, "Do you want to update this event?", "Confirm Add", JOptionPane.YES_NO_OPTION);
                if (n == 0) {
                    ps.executeUpdate();
                    ps1.execute();
                    JOptionPane.showMessageDialog(this, "Event Added!");
                    dispose();
                    this.a.LoadEvents();
                }
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

