    boolean checkConflict(String id, String start, String end){
        int count = 0;
        try {
            DBConnection.init();
            Connection c = (Connection) DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            ps = c.prepareStatement("SELECT * FROM events WHERE ((ev_start <= '" + start + "' AND ev_end > '" + start + "') OR (ev_start < '" + end + "' AND ev_end >= '" + end + "')) AND ID !="+id+"");
            rs = ps.executeQuery();
            while (rs.next()) {
                count++;
            }
            System.out.println(count);
        } catch (Exception e) {
            System.out.println(e);
        }
        return count != 0;
    }
    
